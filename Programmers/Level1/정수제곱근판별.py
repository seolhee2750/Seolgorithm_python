def solution(n):
    return -1 if n**(1/2) % 1 != 0 else (n**(1/2) + 1) * (n**(1/2) + 1)

print(solution(121))
print(solution(3))

# 파이썬에서 제곱근 구하는 방법은 두 가지
# 1. import math 한 다음에 math.sqrt(n) 하면 n의 제곱근 구할 수 있음
# 2. 라이브러리 사용하지 않고, n**(1/2) 하면 n의 제곱근 구할 수 있음
# (마찬가지로 n**(1/3) 이런 식으로 세제곱근 구할 수도 있음)

# [추가]
# 파이썬 산술 연산자는 특이한게 두 개 있음
# ** : 제곱 (ex. 10 ** 3 == 1000)
# // : 몫 (ex. 10 // 2 == 5) (그냥 /의 경우는 10 / 2 = 5.0 이렇게 뒤에 나머지까지 포함됨)